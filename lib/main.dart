        '/': (context) => Landing(),
        //'/': (context) => HomePage(title: 'Certificate Verification'),
        '/login': (context) => const LoginRegister(),
        '/home': (context) => HomePage(title: 'Certificate Verification'),
      },
      theme: ThemeData(
